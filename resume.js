function dimBrightness() {
    if (body.style.backgroundColor = "white") {
        body.style.backgroundColor = "#f2f2f2";
        let newText = document.getElementsByTagName(button).value;
        newText = "Bright";

    }
    else {
        body.style.backgroundColor = "#f2f2f2";
        let newText = document.getElementsByTagName(button).value;
        newText = "Dim";

    }
}    